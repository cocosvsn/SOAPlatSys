package com.soaplat.cmsmgr.dto;

public class TransferDestination {

	public String type = null;
	public String hostname = null;
	public String port = null;
	public String username = null;
	public String password = null;
	public String desStorageId = null;
	public String desStorageDirId = null;
	public String variableFilePath = null;
	public String fileDate = null;
	
	public String file = null;
	public String folder = null;
	
	
	
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getHostname() {
		return hostname;
	}
	public void setHostname(String hostname) {
		this.hostname = hostname;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDesStorageId() {
		return desStorageId;
	}
	public void setDesStorageId(String desStorageId) {
		this.desStorageId = desStorageId;
	}
	public String getDesStorageDirId() {
		return desStorageDirId;
	}
	public void setDesStorageDirId(String desStorageDirId) {
		this.desStorageDirId = desStorageDirId;
	}
	public String getVariableFilePath() {
		return variableFilePath;
	}
	public void setVariableFilePath(String variableFilePath) {
		this.variableFilePath = variableFilePath;
	}
	public String getFileDate() {
		return fileDate;
	}
	public void setFileDate(String fileDate) {
		this.fileDate = fileDate;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public String getFolder() {
		return folder;
	}
	public void setFolder(String folder) {
		this.folder = folder;
	}
	
	
}
